import { NextRequest, NextResponse } from "next/server";
import { getSupabaseAdmin } from "@/lib/supabase/server";
import {
  getUserBySupabaseId,
  createChatMessage,
  deductTokens,
} from "@wacke/db";
import { resolveAuthUserId } from "@/lib/auth-api";

export const runtime = "nodejs";
export const dynamic = "force-dynamic";

const SACRE_COST = 10;

/**
 * POST /api/chat/sacre
 * Deducts 10 tokens and inserts a generated sacre message into the stream chat.
 */
export async function POST(req: NextRequest) {
  try {
    const authUserId = await resolveAuthUserId(
      req.headers.get("Authorization")
    );
    if (!authUserId) {
      return NextResponse.json({ error: "Session invalide" }, { status: 401 });
    }

    const { prefix, core, suffix, streamId } = await req.json();

    if (!prefix || !core || !suffix || !streamId) {
      return NextResponse.json(
        { error: "Paramètres de sacre manquants" },
        { status: 400 }
      );
    }

    const user = await getUserBySupabaseId(authUserId);
    if (!user) {
      return NextResponse.json({ error: "Utilisateur introuvable" }, { status: 404 });
    }

    const content = `${prefix} ${core} ${suffix}! 🤬`;

    // Check if streamId is a valid UUID
    const isValidStreamId = streamId && /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i.test(streamId);

    // 1. Deduct tokens
    try {
      await deductTokens({
        userId: user.id,
        amount: SACRE_COST,
        reason: `Sacre généré: ${core}`,
        streamId: isValidStreamId ? streamId : undefined,
      });
    } catch (err: any) {
      return NextResponse.json(
        { error: err.message || "Fonds insuffisants" },
        { status: 402 }
      );
    }

    // 2. Create chat message
    let message;
    if (isValidStreamId) {
      message = await createChatMessage({
        streamId,
        userId: user.id,
        content,
        isSacre: true, // Swears generated bypass normal filter since they are authentic
      });
    } else {
      message = {
        id: `mock-msg-${Date.now()}`,
        streamId,
        userId: user.id,
        content,
        isSacre: true,
        isDeleted: false,
        createdAt: new Date(),
      };
    }



    return NextResponse.json({ success: true, message });
  } catch (error) {
    console.error("[sacre API error]", error);
    return NextResponse.json({ error: "Erreur serveur" }, { status: 500 });
  }
}
